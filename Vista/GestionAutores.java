    private void jbuttonagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonagregarActionPerformed
         if((jtextfieldnombre.getText().contentEquals(""))||
             (jtextfieldapellidos.getText().contentEquals("")||
             (jtextfieldcedula.getText().contentEquals(""))||
             (jtextfieldid.getText().contentEquals(""))||
             (jtextfieldfecha.getText().contentEquals(""))||
             (jtextfieldemail.getText().contentEquals("")))){
         JOptionPane.showMessageDialog(null, "algunos campos estan vacios", "Revise todos los campos!",JOptionPane.WARNING_MESSAGE);
         
         }else{
                 String nombre =this.jtextfieldnombre.getText();
                 String apellido = this.jtextfieldapellidos.getText();
                 String cedula = this.jtextfieldcedula.getText();
                 String id = this.jtextfieldid.getText();
                 String fecha = this.jtextfieldfecha.getText();
                 String email = this.jtextfieldemail.getText();
                 
         try{
             Autores autor =new Autores (id , nombre , apellido , cedula, fecha , email);
             this.limpiarCampos();
             this.listaautores.add(autor);
             JOptionPane.showMessageDialog(null, "Datos agregados correctamente");
             
              }catch(Exception e){
                  JOptionPane.showMessageDialog(null,"Datos no agregados","Error", JOptionPane.WARNING_MESSAGE);
              }
         llenarTabla(); 
      JOptionPane.showMessageDialog(null, "Datos agregados correctamente"); 
       }     
    }//GEN-LAST:event_jbuttonagregarActionPerformed

    private void jbuttonborrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonborrar1ActionPerformed
        int fila = this.jtableautores.getSelectedRow();
        if (fila == -1){
         JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
    
         }else{
          JDialog.setDefaultLookAndFeelDecorated(true);
          int resp = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de eliminar?" , "Eliminando..." , JOptionPane.YES_NO_OPTION , JOptionPane.QUESTION_MESSAGE);
          
          if (resp == JOptionPane.YES_OPTION){
              listaautores.remove(fila);
              llenarTabla();
          }
        }      
    }//GEN-LAST:event_jbuttonborrar1ActionPerformed

    private void jbuttoneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttoneditarActionPerformed
     int fila = this.jtableautores.getSelectedRow();
      if (fila == -1) {
          JOptionPane.showMessageDialog(rootPane,"Seleccione un registro de la tabla");
          
     }else{
          String id = (String) this.jtableautores.getValueAt(fila, 0);
          String nom = (String) this.jtableautores.getValueAt(fila, 1);
          String ape = (String) this.jtableautores.getValueAt(fila, 2);
          String ced = (String) this.jtableautores.getValueAt(fila, 3);
          String fechaNac = (String) this.jtableautores.getValueAt(fila, 4);
          String email= (String) this.jtableautores.getValueAt(fila, 5);
          
             jtextfieldnombre.setText(nom);
             jtextfieldid.setText(id);
             jtextfieldapellidos.setText(ape);
             jtextfieldcedula.setText(ced);
             jtextfieldfecha.setText(fechaNac);
             jtextfieldemail.setText(email);
             indice =fila;
             }
    }//GEN-LAST:event_jbuttoneditarActionPerformed

    private void jbuttonactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonactualizarActionPerformed
     if((jtextfieldnombre.getText().isEmpty())||
             (jtextfieldapellidos.getText().isEmpty())||
             (jtextfieldid.getText().isEmpty())||
             (jtextfieldemail.getText().isEmpty())||
             (jtextfieldcedula.getText().isEmpty())||
             (jtextfieldfecha.getText().isEmpty())|| indice==-1){     
                JOptionPane.showMessageDialog(null, "Algunos campos estan vacios", "Actualizar...", JOptionPane.WARNING_MESSAGE);
                limpiarCampos();
            }else{
                listaautores.get(indice).setNombres(jtextfieldnombre.getText());
                listaautores.get(indice).setId(jtextfieldid.getText());
                listaautores.get(indice).setApellidos(jtextfieldapellidos.getText());
                listaautores.get(indice).setEmail(jtextfieldemail.getText());
                listaautores.get(indice).setFechaNac(jtextfieldfecha.getText());
                listaautores.get(indice).setCedula(jtextfieldcedula.getText());
                
                llenarTabla();
                limpiarCampos();
                
            }  
    }//GEN-LAST:event_jbuttonactualizarActionPerformed

